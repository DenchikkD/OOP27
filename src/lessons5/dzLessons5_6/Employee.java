public class Employee {

    String name;
    String sName;
    int price;
    int bankAccount;
    String sex;
    String department;

    public Employee(String name, String sName, int price, int bankAccount, String sex, String department) {
        this.name = name;
        this.sName = sName;
        this.price = price;
        this.bankAccount = bankAccount;
        this.sex = sex;
        this.department = department;
    }
}
